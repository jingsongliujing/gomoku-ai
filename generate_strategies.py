def create_strategy_doc(filename: str, title: str, strategy_text: str, 
                       style: str, description: str):
    """创建策略文档"""
    content = f"""# {title}

## 策略描述

{strategy_text}

## 策略风格

{style}

## 说明

{description}

## 使用方法

将以下策略文本复制到飞书多维表格的"自然语言策略"字段：

```
{strategy_text}
```
"""
    
    filepath = os.path.join(OUTPUT_DIR, filename)
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)


